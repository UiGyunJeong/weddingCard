    $('.js-2014').click(function() {
        var photo = $('.y2014');
        var icon = $('.i-2014');
        
        photo.slideToggle(400);
        
        if (icon.hasClass('ion-chevron-down')) {
            icon.addClass('ion-chevron-up');
            icon.removeClass('ion-chevron-down');
        } else {
            icon.addClass('ion-chevron-down');
            icon.removeClass('ion-chevron-up');
        }        
    });

